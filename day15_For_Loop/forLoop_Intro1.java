package day15_For_Loop;

public class forLoop_Intro1 {
    public static void main(String[] args) {
        for(int i=1;i<=10;i++){// i-- goes for ever
            System.out.print("Hello world ");
        }
        System.out.println();
        for(int k=1;k<=5;k+=2){
            System.out.println(k);
        }
    }
}

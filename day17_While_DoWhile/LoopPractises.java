package day17_While_DoWhile;

public class LoopPractises {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        int j = 1;
        while (j <= 10) {
            System.out.println(j);
            j++;
        }
        int k=1;
        do {
            System.out.print(k);
            k++;
        } while(k<=10);
    }
}
